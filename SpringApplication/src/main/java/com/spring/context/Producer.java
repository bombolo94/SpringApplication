package com.spring.context;

public class Producer
{
  private String id;
  private String schemeID;
  
  public Producer() {}
  
  public String getId()
  {
    return id;
  }
  
  public void setId(String id)
  {
    this.id = id;
  }
  
  public String getSchemeID()
  {
    return schemeID;
  }
  
  public void setSchemeID(String schemeID)
  {
    this.schemeID = schemeID;
  }
}