package com.spring.values;

public class PropertyValues {
  private String name;
  private String val;
  
  public String getName() {
    return name;
  }
  
  public void setName(String name) {
    this.name = name;
  }
  
  public String getVal() {
    return val;
  }
  
  public void setVal(String val) {
    this.val = val;
  }
  
  public PropertyValues() {}
}